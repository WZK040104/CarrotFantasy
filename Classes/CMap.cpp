#include"CMap.h"

CMap::CMap(char inputMap[][Map_x])
{
	for (int y = 0; y < Map_y; y++)
		for (int x = 0; x < Map_y; x++)
			map[y][x] = inputMap[y][x];
}
CMap::~CMap() {};	// 对象销毁时执行的清理工作，例如释放资源

int CMap::getMap(int x, int y)
{
	return map[x][y];
}

